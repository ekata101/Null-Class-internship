export const ask = async (req, res) => {
  try {
    const db = await getDb();
    const userId = req.userId;
    const question = req.body.title;

    if (!question) return res.status(400).json({ message: "Question is required" });

    const sub = await db.get("SELECT daily_limit FROM subscriptions WHERE user_id = ?", [userId]);
    if (!sub) return res.status(403).json({ message: "No subscription found" });

    const { count } = await db.get(
      "SELECT COUNT(*) AS count FROM questions WHERE user_id = ? AND DATE(created_at) = DATE('now')",
      [userId]
    );

    if (count >= sub.daily_limit) {
      return res.status(403).json({
        message: `Daily limit reached (${sub.daily_limit} questions/day). Please upgrade your plan.`,
      });
    }

    const aiAnswer = generateAiAnswer(question);
    await db.run(
      "INSERT INTO questions (user_id, title, ai_answer, created_at) VALUES (?, ?, ?, datetime('now'))",
      [userId, question, aiAnswer]
    );

    res.json({ message: "Question posted successfully", aiAnswer });
  } catch (err) {
    res.status(500).json({ message: "Failed to post question", error: err.message });
  }
};
